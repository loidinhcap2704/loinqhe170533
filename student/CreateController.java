class CreateUsername {
    public String createUsername(String name){
            String raw_name = VNCharacterUtils.removeAccent(name).toLowerCase();
            String[] partname = raw_name.split(" ");
            String username = partname[partname.length-1];
            for(String s: partname){
                username+=s.charAt(0);
            }
            username=username.substring(0,username.length()-1);
            return username;
    }
}

